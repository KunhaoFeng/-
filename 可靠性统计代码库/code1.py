import numpy as np
from scipy.stats import norm

# ==========================================
# 1. 录入试验数据 (表4)
# ==========================================
data_0 = np.array([45.06, 45.49, 44.13, 45.16, 44.98, 44.45, 44.74, 44.99, 46.06, 45.79,
                   44.43, 45.88, 45.12, 44.86, 45.12, 44.81, 44.84, 45.37, 45.34, 45.35,
                   45.10, 44.48, 45.12, 45.42, 45.04, 45.22, 45.12, 44.78])
data_1 = np.array([14.60, 14.84, 14.60, 14.66, 14.70, 14.59, 14.78, 14.49, 14.75, 14.20,
                   14.60, 15.20, 15.03, 14.84, 14.60, 14.84, 14.60, 14.66, 14.70, 14.59,
                   14.78, 14.49, 14.80, 15.00, 14.77, 14.90, 15.03, 14.78])
data_2 = np.array([14.63, 14.70, 14.64, 14.72, 14.70, 14.68, 14.68, 14.63, 14.60, 14.90,
                   14.71, 15.00, 14.74, 15.00, 14.63, 14.71, 14.64, 14.75, 14.71, 14.70,
                   14.70, 14.63, 14.75, 14.90, 14.75, 14.90, 14.70, 14.84])
data_3 = np.array([14.66, 14.86, 14.62, 14.68, 14.70, 14.68, 14.86, 14.62, 14.62, 14.86,
                   14.49, 15.20, 14.68, 14.90, 14.64, 14.86, 14.60, 14.66, 14.74, 14.70,
                   14.86, 14.60, 14.88, 14.90, 14.68, 14.70, 14.66, 14.90])

data_all = [data_0, data_1, data_2, data_3]

# 提取各组数据的统计特征
n_j = [len(x) for x in data_all]  # 样本量
mu_j = [np.mean(x) for x in data_all]  # 样本均值
s_j = [np.std(x, ddof=1) for x in data_all]  # 样本标准差 (无偏估计 ddof=1)

print("=" * 60)
print("【第一部分】中间统计量输出")
print("-" * 60)
for i in range(4):
    print(f"{i}# 轴线 | 样本量 (n): {n_j[i]} | 样本均值 (μ): {mu_j[i]:.4f} | 样本标准差 (s): {s_j[i]:.4f}")

# 各轴线锁定深度的设计要求阈值
d_j = [44.0, 14.5, 14.5, 14.5]

# ==========================================
# 2. 解析法计算理论可靠度点估计
# ==========================================
n0 = 2750  # 火工品实际试验次数
k0 = 0  # 零失效

# (1) 火工品切割器可靠度点估计 (IMML法)
R1_hat = 1.0 - 1.0 / (n0 + 1)

# (2) 太阳翼展开终了锁定的可靠度点估计 (正态分布假设)
R2_j_hat = np.zeros(4)
for j in range(4):
    # P(X >= d_j) = 1 - Phi((d_j - mu_j) / s_j) = Phi((mu_j - d_j) / s_j)
    R2_j_hat[j] = norm.cdf((mu_j[j] - d_j[j]) / s_j[j])

R2_hat = np.prod(R2_j_hat)

# (3) 系统总可靠度点估计
R_sys_hat = R1_hat * R2_hat

print("\n" + "=" * 60)
print("【第二部分】系统可靠度点估计 (解析法)")
print("-" * 60)
print(f"火工品切割器点估计 (R1_hat): {R1_hat:.6f}")
for j in range(4):
    print(f"{j}# 轴线锁定点估计 (R2_{j}_hat): {R2_j_hat[j]:.6f}")
print(f"太阳翼综合锁定点估计 (R2_hat): {R2_hat:.6f}")
print(f"太阳翼结构机构总点估计 (R_sys_hat): {R_sys_hat:.6f}")

# ==========================================
# 3. 贝叶斯后验抽样计算可靠度置信下限 (Monte Carlo)
# ==========================================
M = 100000  # 增加抽样次数提高精度
gamma = 0.7  # 置信水平
alpha = 1 - gamma

np.random.seed(2026)  # 设定随机种子保证结果可复现

# 生成随机样本
u = np.random.beta(n0 - k0, k0 + 1, M)  # 火工品可靠度后验抽样 (IMML对应Beta(n0,1))
z = np.random.standard_normal((4, M))  # 正态分布抽样
v = np.zeros((4, M))
for j in range(4):
    v[j, :] = np.random.chisquare(n_j[j] - 1, M)  # 卡方分布抽样

# 矢量化计算 M 次抽样的可靠度评估值 (修正后的正确公式)
R2_samples = np.ones(M)
for j in range(4):
    sigma_j = s_j[j] * np.sqrt(n_j[j] - 1) / np.sqrt(v[j, :])  # 抽样总体标准差σ
    mu_j_sample = mu_j[j] + z[j, :] * sigma_j / np.sqrt(n_j[j])  # 抽样总体均值μ (关键修正: 除以√n)
    R2_j_sample = norm.cdf((mu_j_sample - d_j[j]) / sigma_j)  # 计算单轴线可靠度
    R2_samples *= R2_j_sample

r_samples = u * R2_samples  # 系统可靠度抽样值

# 规范计算分位数作为置信下限 (method='lower'与原代码取法一致)
R_lower_bound = np.percentile(r_samples, alpha * 100, method='lower')

print("\n" + "=" * 60)
print("【第三部分】可靠度置信下限评估 (贝叶斯后验抽样)")
print("-" * 60)
print(f"仿真抽样次数 (M): {M}")
print(f"置信水平: {gamma}")
print(f"太阳翼结构机构可靠度置信下限: {R_lower_bound:.6f}")
print("=" * 60)
